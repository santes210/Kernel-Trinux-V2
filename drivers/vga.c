/* Carga la fuente 8x8 a la memoria de fuentes VGA (plano 2 en 0xA0000)
 * y reprograma el CRTC para que cada caracter ocupe 8 scanlines.
 *
 * Es magia VGA estándar — los puertos y secuencias son fijos. Si esta
 * función no se llama, el modo de texto sigue siendo 80x25.
 */
void vga_set_8x8_mode(void)
{
    volatile uint8_t *font_ram = (volatile uint8_t *)0xA0000;

    /* === FASE A: cambiar a "modo fuente" para acceder al plano 2 === */

    /* Sequencer (0x3C4): seleccionar plano 2 a escritura */
    outb(0x3C4, 0x02); outb(0x3C5, 0x04);   /* Map Mask: solo plano 2 */
    outb(0x3C4, 0x04); outb(0x3C5, 0x07);   /* Memory Mode: sequential, no chain-4 */

    /* Graphics Controller (0x3CE): leer del plano 2, modo 0 */
    outb(0x3CE, 0x04); outb(0x3CF, 0x02);   /* Read Map Select: plano 2 */
    outb(0x3CE, 0x05); outb(0x3CF, 0x00);   /* Mode: write mode 0 */
    outb(0x3CE, 0x06); outb(0x3CF, 0x04);   /* Misc: map 0xA0000-0xAFFFF, alpha */

    /* === FASE B: copiar la fuente 8x8 ===
     * VGA reserva 32 bytes por glifo en la RAM de fuentes (aunque el
     * glifo en sí sea de 8 o 16 bytes). Copiamos los primeros 7 bytes
     * de cada slot y dejamos el byte 7 (última fila) en 0 para crear
     * un row de "respiro" entre líneas — evita que glifos altos toquen
     * la fila siguiente. Los 24 bytes restantes también van en 0. */
    for (int g = 0; g < 256; g++) {
        for (int row = 0; row < 7; row++)
            font_ram[g * 32 + row] = vga_font_8x8[g * 8 + row];
        /* fila 7 forzada a blanco para padding entre lineas */
        font_ram[g * 32 + 7] = 0;
        for (int row = 8; row < 32; row++)
            font_ram[g * 32 + row] = 0;
    }

    /* === FASE C: restaurar el modo texto normal === */

    outb(0x3C4, 0x02); outb(0x3C5, 0x03);   /* Map Mask: planos 0 y 1 (char + attr) */
    outb(0x3C4, 0x04); outb(0x3C5, 0x03);   /* Memory Mode: vuelta a modo texto */

    outb(0x3CE, 0x04); outb(0x3CF, 0x00);   /* Read Map: plano 0 */
    outb(0x3CE, 0x05); outb(0x3CF, 0x10);   /* Mode: write mode 0, host -> attr */
    outb(0x3CE, 0x06); outb(0x3CF, 0x0E);   /* Misc: map 0xB8000-0xBFFFF, text */

    /* === FASE D: programar CRTC para 80x50 ===
     * Maximum Scan Line (reg 0x09): low 5 bits = (lines_per_char - 1).
     * Para fuente 8x8 -> 7. Mantenemos los bits altos (double-scan, etc).
     * Vertical Display End (reg 0x12): no hace falta tocar — el panel
     * sigue siendo 400 lineas, y 50 filas x 8 = 400 cabe perfecto. */
    outb(0x3D4, 0x09);
    uint8_t msl = inb(0x3D5);
    msl = (msl & 0xE0) | 0x07;   /* low 5 bits = 7 */
    outb(0x3D4, 0x09); outb(0x3D5, msl);

    /* Cursor scanline start/end: ajustar al nuevo tamaño de cell */
    outb(0x3D4, 0x0A); outb(0x3D5, 0x06);   /* cursor start scanline */
    outb(0x3D4, 0x0B); outb(0x3D5, 0x07);   /* cursor end scanline */
}

/* ====================================================================
 *  display_init: cambia al backend de framebuffer si esta disponible.
 *
 *  Se llama desde kernel_main DESPUES de vmm_init (porque fb_init
 *  necesita vmm_map_page para mapear el FB que esta en addr alta).
 *  Si funciona, todas las llamadas vga_* siguientes despachan a fb_*.
 *  Si falla, seguimos en modo VGA texto sin cambios.
 *
 *  Devuelve true si conmuto a modo grafico.
 * ==================================================================== */
bool display_init(uint32_t mb_magic, uint32_t mb_info_addr)
{
    if (g_use_fb) return true;
    if (fb_init(mb_magic, mb_info_addr)) {
        g_use_fb = true;
        /* Sync color y limpiar pantalla nueva. */
        fb_set_color(cur_color);
        fb_clear();
        return true;
    }
    return false;
}

bool display_using_fb(void) { return g_use_fb; }
int  display_text_cols(void) { return g_use_fb ? fb_text_cols() : VGA_WIDTH; }
int  display_text_rows(void) { return g_use_fb ? fb_text_rows() : VGA_HEIGHT; }
